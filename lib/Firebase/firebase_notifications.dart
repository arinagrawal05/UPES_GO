      Navigator.push(
          context,
          MaterialPageRoute(
              builder: (context) => OutPassViewScreen(
                  // id: message.data['id'],
                  )));
    }
  }

  Future forgroundMessage() async {
    await FirebaseMessaging.instance
        .setForegroundNotificationPresentationOptions(
      alert: true,
      badge: true,
      sound: true,
    );
  }

